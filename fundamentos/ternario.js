//Operador ternário com função arrow
const resultado = nota => nota >= 7 ? 'Aprovado' : 'Reprovado'
//se der verdadeiro (?) retorna aprovado, se falso (:) retorna 'Reprovado

console.log(resultado(7.1))
console.log(resultado(6.1))

